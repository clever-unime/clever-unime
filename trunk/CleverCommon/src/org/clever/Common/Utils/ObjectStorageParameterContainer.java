package org.clever.Common.Utils;

/**
 *
 * @author Riccardo Di Pietro 2014
 */
public class ObjectStorageParameterContainer {
 
    
    private String X_Account_Meta;
    private String name;

    
    
    
    public ObjectStorageParameterContainer() {
        this.X_Account_Meta = "";
        this.name = "";
    }
    
     
    

    public String getX_Account_Meta() {
        return X_Account_Meta;
    }

    public void setX_Account_Meta(String X_Account_Meta) {
        this.X_Account_Meta = X_Account_Meta;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
    
    
    
    
    
    
    
    
    
}
